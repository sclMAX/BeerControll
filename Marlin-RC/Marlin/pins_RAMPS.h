      #define BTN_EN2 33
      #define BTN_ENC 35
      #define SD_DETECT_PIN 49
      #define KILL_PIN 41
    #else
      // Beeper on AUX-4
      #define BEEPER_PIN 33
      #define BTN_EN1 37
      #define BTN_EN2 35
      #define BTN_ENC 31  // the click
    #endif
  #endif // NEWPANEL
#endif // ULTRA_LCD
